def update_tiers(slug, tiers):
    f = ROOT / 'product' / slug / 'index.html'
    h = f.read_text(encoding='utf-8')
    m = re.search(r'(var\s+TIERS\s*=\s*)(\[.*?\])(\s*;)', h, re.DOTALL)
    if not m:
        print(f'FAIL {slug}: TIERS not found')
        return False
    new_json = json.dumps(tiers, ensure_ascii=False)
    new_h = h[:m.start(2)] + new_json + h[m.end(2):]
    f.write_text(new_h, encoding='utf-8')
    print(f'OK {slug}: {len(tiers)} tiers, prices = {", ".join(t["price"] for t in tiers)}')
    return True


update_tiers('content-product', CONTENT_TIERS)
update_tiers('product-statii', STATII_TIERS)
update_tiers('youtube-product', YOUTUBE_TIERS)
